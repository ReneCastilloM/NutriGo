    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int fila = Tabla.getSelectedRow();
        if (fila != -1){
            int x = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro que desea eliminar este alimento?", "Eliminar alimento", JOptionPane.YES_NO_OPTION);
            if (x == 0){
                try{
                    int row = Tabla.getSelectedRow();
                    String reg = (String) Tabla.getValueAt(row, 0);
                    int registro = Integer.parseInt(reg);
                    Conectar con = new Conectar();
                    Connection co = con.conexion();
                    Statement stm = co.createStatement();
                    String eliminar = "DELETE FROM comidas_predeterminadas WHERE id=" + registro;
                    stm.executeUpdate(eliminar);
                    JOptionPane.showMessageDialog(null, "Registro eliminado con exito");
                    
                    DefaultTableModel modelo=(DefaultTableModel) Tabla.getModel();
                    int filas = Tabla.getRowCount();
                    for (int i = 0;filas>i; i++)
                    {
                        modelo.removeRow(0);
                    }
                    
                    String sql = "SELECT id,calorias,comida,titulo FROM comidas_predeterminadas";
                    String []datos_1000 = new String [4];
                    Statement st;
                    st = co.createStatement();
                    ResultSet rs = st.executeQuery(sql);
                    while(rs.next()){
                        datos_1000[0] =rs.getString("id");
                        datos_1000[1]=rs.getString("calorias");
                        datos_1000[2]=rs.getString("comida");
                        datos_1000[3]=rs.getString("titulo");
                        modelo.addRow(datos_1000);
                    }
                }
                catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Error al eliminar el registro, intentelo nuevamente");
                }
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Para eliminar un alimento, primero debe seleccionar su fila en la tabla superior");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

